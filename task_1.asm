org 0x100                ; Вказуємо, що це програма .COM

section .data
    a db 5               ; Визначаємо a = 5
    b db 3               ; Визначаємо b = 3
    c db 2               ; Визначаємо c = 2
    resultMsg db 'Result: ', 0 ; Текст із завершувачем $
    result db '?', '$'          ; Місце для результату (спочатку '?')

section .text
_start:
    ; Обчислення: a + (b - c)
    mov al, [b]          ; Завантажуємо b в AL
    sub al, [c]          ; Віднімаємо c від AL
    add al, [a]          ; Додаємо a до AL

    ; Конвертуємо результат у ASCII символ
    add al, 30h          ; Перетворюємо число в ASCII символ
    mov [result], al     ; Зберігаємо результат у місце '?'

    ; Виведення тексту "Result: "
    mov ah, 09h          ; Функція DOS для виведення рядка
    lea dx, resultMsg    ; Завантажуємо адресу рядка resultMsg у DX
    int 21h              ; Виклик DOS-переривання

    ; Виведення результату (символу)
    ; mov dl, al           ; Завантажуємо ASCII-символ результату у DL
    ; mov ah, 02h          ; Функція DOS для виведення символу
    ; int 21h              ; Виклик DOS-переривання

    ; Завершення програми
    mov ax, 4c00h        ; Функція DOS для завершення програми
    int 21h              ; Виклик DOS-переривання

